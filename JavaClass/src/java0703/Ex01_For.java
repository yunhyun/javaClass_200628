package java0703;

public class Ex01_For {

	public static void main(String[] args) {
		// for문 
		// 1부터 3까지 출력하기 
		int num = 0;
		num = 1;
		System.out.println(num);
		num = 2;
		System.out.println(num);
		num = 3;
		System.out.println(num);
		
		
		
		
		System.out.println("for문 시작 ");
		int i = 0;
		for(i=1; i<=100; i++) {
			System.out.println("for문 내부");
			System.out.println(i);
		}
		System.out.println("for문 종료 ");
		System.out.println(i);
		
		
		
	}

}
