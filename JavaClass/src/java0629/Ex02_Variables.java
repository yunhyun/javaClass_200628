package java0629;

public class Ex02_Variables {

	public static void main(String[] args) {
		/*
		 * 변수를 활용한 간단한 계산
		 * 	1. 정수형 변수 number를 선언하여 정수 대입 
		 *  2. 정수형 변수 result 선언 
		 *  3. number에 10을 더하여 result 변수에 대입 
		 *  4. result 변수가 가지고 있는 값 출력  
		 */
		// 정수형 변수 선언(초기값 0)
		int number = 0;
		int result = 0;
		// number 변수에 10 대입 
		number = 50;
		// 3. number에 10을 더하여 result 변수에 대입 
		result = number + 10;
		// result = 10 + 10;
		System.out.println(result);
		System.out.println(result + 10);
		
		
		
		
	}

}
